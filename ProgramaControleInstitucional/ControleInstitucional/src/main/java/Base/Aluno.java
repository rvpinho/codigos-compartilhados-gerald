package Base;

/**
 *
 * @author Rafael
 */
public class Aluno {
    
    private int ID;
    private String nome;   

    public Aluno(int ID, String nome, String email, int turma) {
        this.ID = ID;
        this.nome = nome;
        this.email = email;
        this.turma = turma;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getTurma() {
        return turma;
    }

    public void setTurma(int turma) {
        this.turma = turma;
    }
    private String email;
    private int turma;
}
